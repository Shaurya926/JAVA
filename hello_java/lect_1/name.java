public class name {
   public static void main(String[] args){
//        - public
//        - Access modifier → means this method is accessible from anywhere.
//                - The JVM must be able to call it, so it has to be public.
//        - static
//        - Belongs to the class, not to an object.
//        - JVM doesn’t create an object of your class to run the program; it directly calls this method.
//                - That’s why main must be static.
//        - void
//        - Return type → means this method doesn’t return any value.
//                - The JVM just executes it and doesn’t expect a result back.
//                - main
//                - The special method name recognized by JVM as the starting point of the program.
//                for example:-
                System.out.println("My name is Shaurya Dubey");
    }
}
